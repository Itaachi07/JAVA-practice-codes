public class Ascii {
    public static void main(String[] args) {
        //Assigning a Variable to the int Variable
        int a = 'a';
        System.out.println(a);
        
        char ch = 'b';
        int b= ch;
        System.out.println(ch+" --"+ b);

        //Using typecasting
        int b1 =(int)ch;
        System.out.println(b1);
    }
}
